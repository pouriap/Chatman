    public String getOutgoingText(){
        String s = textAreaOutgoing.getText();
        if(!s.contains("<div id=\"text\">"))
            return "";
        return s.substring((s.indexOf("<div id=\"text\">"))+15, (s.indexOf("</div>"))-1).trim();
    }
    
