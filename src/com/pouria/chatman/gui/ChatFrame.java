    private void textAreaChatHistoryHyperlinkUpdate(javax.swing.event.HyperlinkEvent evt) {//GEN-FIRST:event_textAreaChatHistoryHyperlinkUpdate
        //triggered when we click on a link
        if(evt.getEventType() == HyperlinkEvent.EventType.ACTIVATED){
            try{
                String path = evt.getURL().toString();
                
                //open if it was a file
                if(path.contains("file://")){
                    path = path.substring(7);
                    Desktop.getDesktop().open(new File(path));
                }
                //browse if it was a URL
                else
                    Desktop.getDesktop().browse(evt.getURL().toURI());
                
            }catch(IOException e){
                message(Helper.getInstance().getStr("url_open_fail") + e.getMessage());
				Helper.getInstance().log("failed to open url: " + e.getMessage());
            }catch(URISyntaxException e){
                message(Helper.getInstance().getStr("bad_url") + e.getMessage());
				Helper.getInstance().log("failed to open url: " + e.getMessage());
            }
        }
		
    }//GEN-LAST:event_textAreaChatHistoryHyperlinkUpdate

